                <dd>Tenth edition of Tokyo Biennale, entitled <em>Between Man and Matter</em>, is held at the Tokyo Metropolitan Art Museum; travels to the Kyoto Municipal Art Museum, the Aichi Prefectural Art Gallery in Nagoya, and the Fukuoka Prefectural Culture House.</dd>
                <dd>Expo ’70, entitled <em>Progress and Harmony for Mankind</em>, held in Osaka.</dd>
                <dd class="event">Further protests against the renewal of the Japan–US Treaty of Mutual Security and Cooperation.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1969</dt>
                <dd>Museum of Modern Art, Tokyo, relocates to present location in Takebashi.</dd>
                <dd>Christie’s holds its first sale in Asia, in Tokyo.</dd>
                <dd class="event">Student protests against the Vietnam War and American use of bases on Japanese soil culminate in takeover of Tokyo University.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1968</dt>
                <dd><em>Tricks and Vision – Stolen Eyes</em> held at <a href="/spaces/tokyo-gallery/">Tokyo Gallery</a> and Muramatsu Gallery.</dd>
                <dd>Nobuo Sekine creates <em>Phase – Mother Earth</em> in Kobe’s Sumarikyu Park for the First Open Air Contemporary Sculpture Exhibition.</dd>
        		<dd><a href="/appendices/#monoha">Mono-ha</a> active in Tokyo until c. 1972.</dd>
        		<dd class="event">Japan surpasses West Germany to become the world's second-largest economy after the United States.</dd>
              </dl>
            </li>


            <li>
              <dl>
                <dt>1964</dt>
                <dd>Gempei Akasegawa creates exhibition invites featuring reproductions of the ¥1000 note; in 1966 he is put on trial for imitating banknotes (dubbed 'The Thousand-Yen Bill Incident') and found guilty in 1967 with three months suspended sentence; after two appeals the decision was upheld in 1970.</dd>
                <dd class="event">Summer Olympics held in Tokyo.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1963</dt>
                <dd><a href="/appendices/#hiredcenter">Hi Red Center</a> active in Tokyo until 1964.</dd>
                <dd>The National Museum of Modern Art, Kyoto, established.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1962</dt>
                <dd><a href="/appendices/#ndo">Neo-Dada Organizers</a> active in Tokyo until 1964.</dd>
                <dd>Gutai Pinacotheca opens in Osaka.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1960</dt>
                <dd class="event">Widespread protests against renewal of Japan–US Treaty of Mutual Security and Cooperation.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1959</dt>
                <dd>National Museum of Western Art established in Ueno.</dd>
              </dl>
            </li>


            <li>
              <dl>
                <dt>1957</dt>
                <dd><a href="/appendices/#kyushuha">Kyushu-ha</a> active in Fukuoka and Tokyo until 1962.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1956</dt>
                <dd class="event">Japan joins the United Nations.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1955</dt>
                <dd><em>Experimental Outdoor Exhibition of Modern Art to Challenge the Midsummer Sun</em> held in Ashiya Park, Ashiya, Hyogo Prefecture.</dd>
                <dd class="event">Liberal Democratic Party (<span class="sc">LDP</span>) formed. Except for a brief period in the early 1990s, the <span class="sc">LDP</span> governs uninterruptedly until 2009.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1954</dt>
                <dd><a href="/appendices/#gutai">Gutai Art Association</a> forms in Osaka; active in Kansai region until 1972.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1952</dt>
                <dd><a href="/appendices/#momat">Museum of Modern Art, Tokyo</a>, established.</dd>
                <dd>Bridgestone Museum established.</dd>
                <dd>Tokyo Biennale founded; ends 1990.</dd>
                <dd class="event">US occupation of Japan ends, but US retains several islands for military use, including Okinawa.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1951</dt>
                <dd>Museum of Modern Art, Kamakura, established as the first public museum of modern art in Japan.</dd>
                <dd><a href="/appendices/#jikkenkobo">Jikken Kobo</a> (Experimental Workshop) active in Tokyo until c. 1957.</dd>
                <dd class="event">Japan signs Security Treaty with the United States; from 1954 it is known as The Mutual Security Assistance Pact, and from 1960 it was called the Japan-US Treaty of Mutual Security and Cooperation.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1950</dt>
                <dd><a href="/spaces/tokyo-gallery/">Tokyo Gallery</a> opens as the first commercial modern art gallery in Japan.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1949</dt>
                <dd>First edition of the annual <a href="/appendices/#yomiuri">Yomiuri Independent</a> exhibition held in Tokyo; ends 1963.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1948</dt>
                <dd>Monthly Japanese art magazine <em>Bijutsu Techo</em> founded.</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1947</dt>
                <dd class="event">New constitution comes into effect. Article 9 renounces "war as a sovereign right of the nation and the threat or use of force as a means of settling international disputes".</dd>
              </dl>
            </li>

            <li>
              <dl>
                <dt>1945</dt>
                <dd class="event">Following the atomic bombings of Hiroshima and Nagasaki, Japan surrenders in World War II.</dd>
              </dl>
            </li>
            
